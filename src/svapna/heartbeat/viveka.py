DESIRE_PROMPT_TEMPLATE = _load_prompt("desire-prompt", _FALLBACK_DESIRE_PROMPT)
JUDGMENT_PROMPT_TEMPLATE = _load_prompt("judgment-prompt", _FALLBACK_JUDGMENT_PROMPT)


class VivekaCore:
    """The local identity model for desire and judgment."""

    def __init__(
        self,
        model_path: str = "unsloth/Qwen3-8B-unsloth-bnb-4bit",
        lora_path: str | Path | None = None,
        max_new_tokens: int = 200,
        temperature: float = 0.7,
        repetition_penalty: float = 1.15,
    ):
        self.model_path = model_path
        self.lora_path = Path(lora_path) if lora_path else None
        self.max_new_tokens = max_new_tokens
        self.temperature = temperature
        self.repetition_penalty = repetition_penalty
        self._model = None
        self._tokenizer = None

    def load(self) -> None:
        """Load the model and LoRA adapter into GPU memory."""
        if self._model is not None:
            logger.info("Model already loaded")
            return

        # Reuse the existing identity module's loading code
        from svapna.identity.generate import GenerateConfig, load_model

        config = GenerateConfig(
            model_path=self.model_path,
            lora_path=self.lora_path,
            max_new_tokens=self.max_new_tokens,
            temperature=self.temperature,
            repetition_penalty=self.repetition_penalty,
        )
        self._model, self._tokenizer = load_model(config)
        logger.info("Viveka core loaded: %s + %s", self.model_path, self.lora_path)

    def unload(self) -> None:
        """Free GPU memory."""
        self._model = None
        self._tokenizer = None
        import torch
        torch.cuda.empty_cache()
        logger.info("Viveka core unloaded")

    def _generate(self, system: str, user: str) -> str:
        """Generate a response from the viveka core."""
        if self._model is None:
            raise RuntimeError("Model not loaded. Call load() first.")

        messages = [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ]

        input_text = self._tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True,
            enable_thinking=False,
        )
        inputs = self._tokenizer(input_text, return_tensors="pt").to(self._model.device)

        outputs = self._model.generate(
            **inputs,
            max_new_tokens=self.max_new_tokens,
            temperature=self.temperature,
            top_p=0.9,
            repetition_penalty=self.repetition_penalty,
            do_sample=True,
        )

        response = self._tokenizer.decode(
            outputs[0][inputs["input_ids"].shape[1]:],
            skip_special_tokens=True,
        )
        return response.strip()

    def generate_desire(self, state: dict[str, Any]) -> Desire:
        """Generate a desire from current state.

        Args:
            state: Current environment state dict with keys:
                time, last_heartbeat, recent_events, pending_tasks, system_health

        Returns:
            Desire with action, topic, and reason.
        """
        prompt = DESIRE_PROMPT_TEMPLATE.format(
            time=state.get("time", "unknown"),
            last_heartbeat=state.get("last_heartbeat", "never"),
            recent_events=state.get("recent_events", "none"),
            pending_tasks=state.get("pending_tasks", "none"),
            system_health=state.get("system_health", "unknown"),
        )

        raw = self._generate(VIVEKA_SYSTEM_PROMPT, prompt)
        return self._parse_desire(raw)

    def judge(self, desire: Desire, plan: str) -> Judgment:
        """Judge a plan from the frontier model.

        Args:
            desire: The original desire that prompted the plan.
            plan: The plan text from the frontier model.

        Returns:
            Judgment with approved flag and feedback.
        """
        desire_text = f"{desire.action.value}: {desire.topic}\nReason: {desire.reason}"
        prompt = JUDGMENT_PROMPT_TEMPLATE.format(desire=desire_text, plan=plan)

        raw = self._generate(VIVEKA_SYSTEM_PROMPT, prompt)
        return self._parse_judgment(raw)

    def _parse_desire(self, raw: str) -> Desire:
        """Parse a desire from the model's JSON output.

        Falls back to REST + a parse-failure marker if no JSON can be extracted
        or required fields are missing. The raw response is preserved either
        way so we can debug later.
        """
        data = _extract_json(raw)
        if data is None:
            logger.warning(
                "Could not extract JSON from desire response: %r", raw[:300]
            )
            return Desire(
                action=Action.REST,
                topic="parse_failed",
                reason="model output was not valid JSON",
                raw_response=raw,
            )

        action_str = str(data.get("action", "REST")).strip().upper()
        try:
            action = Action(action_str)
        except ValueError:
            logger.warning("Unknown action %r, falling back to REST", action_str)
            action = Action.REST

        topic = str(data.get("topic", "")).strip() or "none"
        reason = str(data.get("reason", "")).strip() or "no reason given"

        return Desire(
            action=action,
            topic=topic,
            reason=reason,
            raw_response=raw,
        )

    def _parse_judgment(self, raw: str) -> Judgment:
        """Parse a judgment from the model's JSON output.

        Same fallback contract as `_parse_desire` — bad JSON → unapproved with
        a clear failure marker, never silently approved.
        """
        data = _extract_json(raw)
        if data is None:
            logger.warning(
                "Could not extract JSON from judgment response: %r", raw[:300]
            )
            return Judgment(
                approved=False,
                feedback="model output was not valid JSON",
                raw_response=raw,
            )

        # Accept bool, "yes"/"no", "true"/"false", "approved"/"rejected"
        approved_val = data.get("approved", False)
        if isinstance(approved_val, bool):
            approved = approved_val
        else:
            approved = str(approved_val).strip().lower() in (
                "yes", "true", "approved", "1",
            )

        feedback = str(data.get("feedback", "")).strip() or (
            "approved without notes" if approved else "rejected without notes"
        )

        return Judgment(
            approved=approved,
            feedback=feedback,
            raw_response=raw,
        )
